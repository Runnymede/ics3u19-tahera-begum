public class Hello {

	public static void main(String[] args) {
		
		//loops the program 10 times 
		//counter starts from 0
		//adds 1 to counter each time it goes through the loop 
		//when counter equals 10 then the loop stops 
		for (int counter = 0; counter < 10; counter++)
		{
			//prints hello each time the computer goes through the loop
			//so it prints hello 10 times
			System.out.println("Hello");
		}

	}

}
